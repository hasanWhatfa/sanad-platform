function unifyArticleCategories(articles: Article[]): Article[] {
  return articles.map(article => {
    const unifiedSet = new Set<string>();

    article.categ.forEach(oldCat => {
      const mappedCats = categoryMap[oldCat];
      if (mappedCats) {
        mappedCats.forEach(cat => unifiedSet.add(cat));
      } else {
        // إذا لم نجد التصنيف في الخريطة نحتفظ به كما هو (اختياري)
        unifiedSet.add(oldCat);
      }
    });

    return {
      ...article,
      categ: Array.from(unifiedSet),
    };
  });
}


